
package Negocio.Planta;


public class TPlanta {
	
	
	private Integer id;
	private Integer numero;
	private Boolean activo;
	
	public TPlanta(Integer id, Integer numero, Boolean activo) {
		this.id = id;
		this.numero = numero;
		this.activo = activo;
	}
	
	public TPlanta(Integer numero, Boolean activo) {
		this.numero = numero;
		this.activo = activo;
	}

	public Integer getId() {
		return id;
	}
	public Integer getNumero() {
		return numero;
	}
	public Boolean getActivo() {
		return activo;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public void setNumero(Integer numero) {
		this.numero = numero;
	}
	public void setActivo(Boolean activo) {
		this.activo = activo;
	}
}