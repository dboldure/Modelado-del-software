


package Negocio.Disco;

public class TDiscoVinilo extends TDisco {
	
	private Integer revoluciones;
	private Integer gramaje;
	private String prensado;
	
	public TDiscoVinilo(Integer id, String nombre, Integer año, Float precio, Integer stock, String genero, Boolean activo, Integer revoluciones, Integer gramaje, String prensado) {
		super(id, nombre, año, precio, stock, genero, activo);
		this.revoluciones = revoluciones;
		this.gramaje = gramaje;
		this.prensado = prensado;
	}
	
	public Integer getRevoluciones() {
		return revoluciones;
	}
	
	public void setRevoluciones(Integer revoluciones) {
		this.revoluciones = revoluciones;
	}
	
	public Integer getGramaje() {
		return gramaje;
	}
	
	public void setGramaje(Integer gramaje) {
		this.gramaje = gramaje;
	}
	
	public String getPrensado() {
		return prensado;
	}
	
	public void setPrensado(String prensado) {
		this.prensado = prensado;
	}
	
}